export function getCaseStudyBySlug(slug) {
  return caseStudyDetails.find((s) => s.id === slug) ?? null;
}

export function getCaseStudyRegion(slug) {
  return featuredCaseStudies.find((f) => f.id === slug)?.region ?? "";
}

export function getAdjacentCaseStudies(slug) {
  const idx = caseStudyDetails.findIndex((s) => s.id === slug);
  if (idx === -1) return { prev: null, next: null, index: -1 };
  return {
    prev: idx > 0 ? caseStudyDetails[idx - 1] : null,
    next: idx < caseStudyDetails.length - 1 ? caseStudyDetails[idx + 1] : null,
    index: idx,
  };
}

export function getCaseStudyMetaDescription(study) {
  const snippet = `${study.headline} ${study.clientProfile}`.replace(/\s+/g, " ").trim();
  if (snippet.length <= 160) return snippet;
  return `${snippet.slice(0, 157)}…`;
}
